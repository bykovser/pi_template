const fs = require("fs");

const path = "C:/Users/sas/.pi/agent/extensions/clerk/data/profile.yaml";
const raw = fs.readFileSync(path, "utf-8");
const lines = raw.split("\n");

// Extract sections manually to rebuild cleanly
let personality = [];
let rules = {};
let userInterests = [];
let telegram = {};
let pingBehavior = {};
let metadata = {};

let section = null;
let currentRuleId = null;
let currentRuleBuffer = [];

for (const line of lines) {
  if (line.startsWith("personality:")) { section = "personality"; personality.push(line); continue; }
  if (line.startsWith("rules:")) { section = "rules"; continue; }
  if (line.startsWith("userInterests:")) { section = "userInterests"; userInterests.push(line); continue; }
  if (line.startsWith("telegram:")) { section = "telegram"; telegram.push(line); continue; }
  if (line.startsWith("pingBehavior:")) { section = "pingBehavior"; pingBehavior.push(line); continue; }
  if (line.startsWith("metadata:")) { section = "metadata"; metadata.push(line); continue; }

  if (section === "personality") personality.push(line);
  else if (section === "rules") {
    currentRuleBuffer.push(line);
    // Detect rule end: either next rule starts with "  - id:" or we're at section end
    if (line.trim().startsWith("- id:")) {
      currentRuleId = line.split(":")[1].trim().replace(/"/g, "");
    }
    // If next indent returns to zero or new section, save rule
    if (line.trim().length > 0 && !line.startsWith("  ") && line.trim().startsWith("-") === false && currentRuleId) {
      // We've moved past rules
      // Actually this logic is flawed. Let me do it differently.
    }
  }
  else if (section === "userInterests") userInterests.push(line);
  else if (section === "telegram") telegram.push(line);
  else if (section === "pingBehavior") pingBehavior.push(line);
  else if (section === "metadata") metadata.push(line);
}

// Simpler approach: parse YAML rules using regex
const ruleRegex = /  - id: "([^"]+)"\n([\s\S]*?)(?=\n  - id:|(?:\n\w)|$)/g;
let match;
const seen = {};
while ((match = ruleRegex.exec(raw)) !== null) {
  const id = match[1];
  const block = match[0];
  // Extract timestamp to get latest
  const tsMatch = block.match(/lastUpdated: "([^"]+)"/);
  const ts = tsMatch ? tsMatch[1] : "";
  if (!seen[id] || ts > seen[id].ts) {
    seen[id] = { block, ts };
  }
}

// Build clean rules
const cleanRules = Object.entries(seen)
  .sort((a, b) => a[0].localeCompare(b[0]))
  .map(([id, data]) => data.block)
  .join("\n");

// Read everything BEFORE rules
const beforeRules = raw.slice(0, raw.indexOf("\n") + 1) + // personality header
  "\n" + personality.slice(1).join("\n") + "\n";

// And everything AFTER rules
const afterRulesRaw = raw.slice(raw.lastIndexOf("userInterests:"));

// Now rebuild
const output = `# Profile: Жанночка
# Auto-generated by Clerk. Do not edit manually.

${personality.join("\n")}

rules:
${cleanRules}

${afterRulesRaw}`;

fs.writeFileSync(path, output, "utf-8");
console.log("✅ Profile cleaned");
console.log(`Rules: ${Object.keys(seen).length} unique (was ~20+ duplicates)`);